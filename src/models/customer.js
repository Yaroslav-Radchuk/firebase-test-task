class Customer {
  constructor(
    id,
    firstname,
    lastname,
    phonenumber,
    email,
    date,
    file,
  ) {
    this.id = id;
    this.firstname = firstname;
    this.lastname = lastname;
    this.phonenumber = phonenumber;
    this.email = email;
    this.date = date;
    this.file = file;
  }
}

export default Customer;
